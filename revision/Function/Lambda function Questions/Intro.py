''' Lambda function '''

# intro : this function is used when we need to do small work(we dont need to define the function and then give it a task)we can justdo this:>

# ** lambda x, y: x + y **

''' 
> lambda: This is the keyword.

> x, y: These are the arguments. They go between lambda and the colon.

> :: The colon separates the arguments from what the function does.

> x + y: This is the expression. The result of this expression is automatically returned. We don't need to write return function in it.

> A lambda function can have multiple arguments, but only one single expression.'''